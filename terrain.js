// Bilinear lookup in warp grid
function _warpUV(wu, wv) {
    const gx = wu * _WARP_SEGS;
    const gy = wv * _WARP_SEGS;
    const x0 = Math.min(Math.floor(gx), _WARP_SEGS - 1);
    const y0 = Math.min(Math.floor(gy), _WARP_SEGS - 1);
    const x1 = x0 + 1, y1 = y0 + 1;
    const fx = gx - x0, fy = gy - y0;
    const gs = _WARP_SEGS + 1;

    const i00 = (y0 * gs + x0) * 2, i10 = (y0 * gs + x1) * 2;
    const i01 = (y1 * gs + x0) * 2, i11 = (y1 * gs + x1) * 2;

    const tu = _WARP_GRID[i00]   * (1-fx)*(1-fy)
             + _WARP_GRID[i10]   *    fx *(1-fy)
             + _WARP_GRID[i01]   * (1-fx)*   fy
             + _WARP_GRID[i11]   *    fx *   fy;

    const tv = 0 + (
               _WARP_GRID[i00+1] * (1-fx)*(1-fy)
             + _WARP_GRID[i10+1] *    fx *(1-fy)
             + _WARP_GRID[i01+1] * (1-fx)*   fy
             + _WARP_GRID[i11+1] *    fx *   fy);

    return { tu, tv };
}

// ─── Mesh construction ────────────────────────────────────────────────────────
function _buildMesh(elevations, texture) {
    const geom  = new THREE.PlaneGeometry(SCENE_W, SCENE_D, MESH_SEGS, MESH_SEGS);
    const pos   = geom.attributes.position;
    const uv    = geom.attributes.uv;
    const verts = MESH_SEGS + 1;

    for (let row = 0; row <= MESH_SEGS; row++) {
        for (let col = 0; col <= MESH_SEGS; col++) {
            const idx = row * verts + col;

            const e = DEM_E_MIN + (col / MESH_SEGS) * (DEM_E_MAX - DEM_E_MIN);
            const n = DEM_N_MAX - (row / MESH_SEGS) * (DEM_N_MAX - DEM_N_MIN);

            const sx = e - ORIGIN_X;
            const sz = -(n - ORIGIN_Y);
            const sy = _sampleDEM(elevations, e, n);

            pos.setXYZ(idx, sx, sy, sz);

            const wu = col / MESH_SEGS;
            const wv = row / MESH_SEGS;
            const { tu, tv } = _warpUV(wu, wv);
            uv.setXY(idx, tu, tv);
        }
    }

    pos.needsUpdate = true;
    uv.needsUpdate  = true;
    geom.computeVertexNormals();

    const mat = new THREE.MeshLambertMaterial({
        map:   texture,
        color: 0xcccccc,
        side:  THREE.FrontSide,
    });

    const mesh = new THREE.Mesh(geom, mat);
    mesh.receiveShadow = true;
    mesh.castShadow    = true;
    mesh.position.set(0, 0, 0);
    return mesh;
}

// ─── DEM bilinear sampler ─────────────────────────────────────────────────────
function _sampleDEM(data, e, n) {
    const u = Math.max(0, Math.min(1, (e - DEM_E_MIN) / (DEM_E_MAX - DEM_E_MIN)));
    const v = Math.max(0, Math.min(1, (DEM_N_MAX - n) / (DEM_N_MAX - DEM_N_MIN)));

    const px = u * (DEM_COLS - 1);
    const py = v * (DEM_ROWS - 1);

    const x0 = Math.floor(px), x1 = Math.min(x0 + 1, DEM_COLS - 1);
    const y0 = Math.floor(py), y1 = Math.min(y0 + 1, DEM_ROWS - 1);
    const fx = px - x0, fy = py - y0;

    const v00 = data[y0 * DEM_COLS + x0];
    const v10 = data[y0 * DEM_COLS + x1];
    const v01 = data[y1 * DEM_COLS + x0];
    const v11 = data[y1 * DEM_COLS + x1];

    return v00 * (1-fx)*(1-fy)
         + v10 *    fx *(1-fy)
         + v01 * (1-fx)*   fy
         + v11 *    fx *   fy;
}