// ─── State ────────────────────────────────────────────────────────────────────
let _mesh       = null;
let _visible    = true;
let _elevations = null;   // cached after initTerrain resolves

// ─── Public API ───────────────────────────────────────────────────────────────
export async function initTerrain(scene, opts = {}) {
    const { onDEMLoaded, onTileProgress } = opts;

    console.log('[Terrain] Loading DEM…');
    _elevations = await _loadDEM();
    console.log(`[Terrain] DEM loaded — ${_elevations.length} samples`);
    if (onDEMLoaded) onDEMLoaded();

    console.log('[Terrain] Fetching satellite tiles…');
    const texture = await _fetchSatelliteTexture({ onTileProgress });
    console.log('[Terrain] Satellite texture ready');

    _mesh = _buildMesh(_elevations, texture);
    _mesh.visible = _visible;
    scene.add(_mesh);

    console.log('[Terrain] Mesh added to scene.');
    return _mesh;
}

export function getTerrainMesh() {
    return _mesh;
}

// Returns terrain elevation (metres ASL) for a UTM EPSG:32734 coordinate.
// Returns 0 if the DEM has not yet loaded.
export function sampleTerrainElevation(e, n) {
    if (!_elevations) return 0;
    return _sampleDEM(_elevations, e, n);
}

// ─── DEM loading ─────────────────────────────────────────────────────────────
async function _loadDEM() {
    const response = await fetch('./dem.tif');
    const buffer   = await response.arrayBuffer();
    const tiff     = await window.GeoTIFF.fromArrayBuffer(buffer);
    const image    = await tiff.getImage();
    const data     = await image.readRasters({ interleave: true });

    // Replace NaN border pixels with minimum valid elevation
    let minElev = Infinity;
    for (let i = 0; i < data.length; i++) {
        if (!isNaN(data[i]) && data[i] < minElev) minElev = data[i];
    }
    if (!isFinite(minElev)) minElev = 0;
    for (let i = 0; i < data.length; i++) {
        if (isNaN(data[i])) data[i] = minElev;
    }

    let maxElev = -Infinity;
    for (let i = 0; i < data.length; i++) {
        if (data[i] > maxElev) maxElev = data[i];
    }
    console.log(`[Terrain] Elevation range: ${minElev.toFixed(1)}m → ${maxElev.toFixed(1)}m`);
    return data;
}

// ─── Satellite texture ────────────────────────────────────────────────────────
async function _fetchSatelliteTexture(opts = {}) {
    const { onTileProgress } = opts;
    const canvasW = TILES_W * TILE_PX;
    const canvasH = TILES_H * TILE_PX;

    const canvas  = document.createElement('canvas');
    canvas.width  = canvasW;
    canvas.height = canvasH;
    const ctx     = canvas.getContext('2d');

    const total = TILES_W * TILES_H;
    let   loaded = 0;

    const fetchTile = (tx, ty) => new Promise((resolve) => {
        const url = `https://mt1.google.com/vt/lyrs=s&x=${tx}&y=${ty}&z=${TILE_Z}`;
        const img = new Image();
        img.crossOrigin = 'anonymous';
        img.onload = () => {
            const px = (tx - TILE_X0) * TILE_PX;
            const py = (ty - TILE_Y0) * TILE_PX;
            ctx.drawImage(img, px, py);
            loaded++;
            if (loaded % 20 === 0 || loaded === total) {
                console.log(`[Terrain] Tiles: ${loaded}/${total}`);
            }
            if (onTileProgress) onTileProgress(loaded, total);
            resolve();
        };
        img.onerror = () => {
            const px = (tx - TILE_X0) * TILE_PX;
            const py = (ty - TILE_Y0) * TILE_PX;
            ctx.fillStyle = '#3a3a3a';
            ctx.fillRect(px, py, TILE_PX, TILE_PX);
            loaded++;
            if (onTileProgress) onTileProgress(loaded, total);
            resolve();
        };
        img.src = url;
    });

    // Batch in groups of 8 to avoid overwhelming the browser
    const BATCH = 8;
    const jobs  = [];
    for (let ty = TILE_Y0; ty <= TILE_Y1; ty++)
        for (let tx = TILE_X0; tx <= TILE_X1; tx++)
            jobs.push([tx, ty]);

    for (let i = 0; i < jobs.length; i += BATCH) {
        await Promise.all(jobs.slice(i, i + BATCH).map(([tx, ty]) => fetchTile(tx, ty)));
    }

    const tex      = new THREE.CanvasTexture(canvas);
    tex.wrapS      = THREE.ClampToEdgeWrapping;
    tex.wrapT      = THREE.ClampToEdgeWrapping;
    tex.anisotropy = 16;
    tex.needsUpdate = true;
    return tex;
}

